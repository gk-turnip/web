<h2>Basic Gameplay</h2>
<p>
  The game is open-ended and sandboxlike, centered around interacting with objects, other users, and the environment through chat, crafting, building, trading, and exploration.
</p>
<p>
  There is also a strong community component. Communal efforts are encouraged and rewarded both by game mechanics and by the goodwill of other players.
</p>

<h2>Game Geography World View</h2>
<p>
  The Knot is composed of many individual pods, each belonging to a linear string; the cross-connections between different strings form the tangle that is the Knot.
</p>

<h2>Game Geography Detail View</h2>
<p>
  An individual pod is a closed, self-sufficient environment with a number of entrances and exits. Pods will contain some combination of resources to be harvested, NPCs or game-provided landmarks, and player-constructed buildings.
</p>

