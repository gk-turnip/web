<h1>Mission Statement</h1>
<p>
  <ul>
    <li>The purpose of this site is to create an mmorpg called Gourdian Knot.</li>
	<li>community-based, community-built, cooperative offbeat fantasy MMO</li>
    <li>For now, the two founders (whom you may know as Jokes and turnip) will be the ultimate arbiter on all content.</li>
    <li>For now, we are soliciting ideas about the game, in the future we may (and I expect we will) get more people involved in the various aspects of development. There are many questions to be answered about this such as "who holds copyright", "is it open source", "what is the license" etc.</li>
    <li>According to <a href="http://en.wikipedia.org/wiki/Massively_multiplayer_online_role-playing_game">this</a>, the cost of developing an mmorpg can be in the area of $10 million dollars,  which is at least $9.999 million dollars more than we are willing to spend building it.  So this may end up being a learning experience, once I find out what the other $9 million (and change) dollars is need to build an mmorpg.</li>
    <li>The intent is to make this scalable to 100,000 simultaneous users, not that we would ever get to that level, or have the server power to handle that many users, but if we are going to build it, we should architect it to handle that kind of load from the start.</li>
