<h1>Second Pod Party</h1>

<h3>What is it?</h3>
<p>
A pod party is where everyone gets on the game at the same time to check out any new game features and discuss the game in general in the in-game chat system.  We had the first pod party a few months back.
</p>

<h3>When is it?</h3>
<p>The date and time have not been set yet.  I'm aiming for around the 12th of June, which is 6 months since the closing of Glitch (and actually 6 months since the start of Gourdian Knot).  Check back here or in the forum for an update once the date/time is set. Leaving a week of possible dates, that would be June 9th to June 15th.</p>

<p>Check the forum <a href="http://gourdianknot.com/forum/viewtopic.php?f=2&t=243&p=2962#p2962">here</a> for a discussion on the second pod party, and put in your preferences for a date/time.</p>

<h3>What is the status of the game?</h3>
<p>
The game is still in the very early stages.  It allowes anyone to register as a new users, and to then login to the game.  Once logged into the game you can do the following:
<ul>
<li>Select an avatar, these don't yet get saved for your next session.</li>
<li>Move around within the game by clicking a point to move to, or using the arrow keys.</li>
<li>Chat with anyone else who is currently in the game.</li>
<li>You should be able to see any of the other players as they come in and out of the game and move around within the game</li>
<li>There is background music as part of the game, and sound effects.</li>
<li>Dandelions have been known to pop up randomly now and then.</li>
<li>It has been know to rain in the pod now and then, but the rain needs a little work :)</li>
<li>You can make verious adjustments to the "pod", like increasing the size displayed on the page, or altering the zoom level.</li>
</ul>
</p>
